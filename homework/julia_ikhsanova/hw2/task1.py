class HW_Task1:
# Даны 2 числа a и b. Получить их сумму, разность и произведение

    a = 100
    b = 2

    def addition(self):
        c = self.a + self.b
        print(c)

    def subtraction(self):
        c = self.a - self.b
        print(c)

    def multiplication(self):
        c = self.a * self.b
        print(c)

new = HW_Task1()
new.addition()
new.subtraction()
new.multiplication()
